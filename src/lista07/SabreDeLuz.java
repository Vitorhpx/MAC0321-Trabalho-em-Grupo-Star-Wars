package lista07;

public class SabreDeLuz {
	private UsuarioDaForca dono;
	private String cristal;
	private String corLamina;
	private String tipoPunho;
	public UsuarioDaForca getDono() {
		return dono;
	}
	public void setDono(UsuarioDaForca dono) {
		this.dono = dono;
	}
	public String getCristal() {
		return cristal;
	}
	public void setCristal(String cristal) {
		this.cristal = cristal;
	}
	public String getCorLamina() {
		return corLamina;
	}
	public void setCorLamina(String corLamina) {
		this.corLamina = corLamina;
	}
	public String getTipoPunho() {
		return tipoPunho;
	}
	public void setTipoPunho(String tipoPunho) {
		this.tipoPunho = tipoPunho;
	}
	

}
